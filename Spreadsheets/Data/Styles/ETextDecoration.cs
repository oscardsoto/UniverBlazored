namespace UniverBlazored.Spreadsheets.Data.Styles
{
    /// <summary>
    /// TextDecoration, from Univer (See doc: https://univer.ai/typedoc/@univerjs/core/enumerations/TextDecoration)
    /// </summary>
    public enum ETextDecoration
    {
        ///<summary>
        /// A dash-like line is used to create the underline.
        ///</summary>
        DASH,
    
        ///<summary>
        /// Heavy double dashes are used to create the underline.
        ///</summary>
        DASH_DOT_DOT_HEAVY,
    
        ///<summary>
        /// A dash followed by a dot is used to create the underline.
        ///</summary>
        DASH_DOT_HEAVY,
    
        ///<summary>
        /// Heavy dashed lines are used to create the underline.
        ///</summary>
        DASHED_HEAVY,
    
        ///<summary>
        /// A long dash-like line is used to create the underline.
        ///</summary>
        DASH_LONG,
    
        ///<summary>
        /// Heavy long dashed lines are used to create the underline.
        ///</summary>
        DASH_LONG_HEAVY,
    
        ///<summary>
        /// A dot followed by a dash is used to create the underline.
        ///</summary>
        DOT_DASH,
    
        ///<summary>
        /// Heavy double dots are used to create the underline.
        ///</summary>
        DOT_DOT_DASH,
    
        ///<summary>
        /// Heavy dotted lines are used to create the underline.
        ///</summary>
        DOTTED,
    
        ///<summary>
        /// Double and solid lines are used to create the underline.
        ///</summary>
        DOUBLE,
    
        ///<summary>
        /// No underlining is performed.
        ///</summary>
        NONE,
    
        ///<summary>
        /// A single line is used to create the underline.
        ///</summary>
        SINGLE,
    
        ///<summary>
        /// The thickness of the underlining line is thicker than normal.
        ///</summary>
        THICK,
    
        ///<summary>
        /// A wave-like line is used to create the underline.
        ///</summary>
        WAVE,
    
        ///<summary>
        /// Heavy double wavy lines are used to create the underline.
        ///</summary>
        WAVY_DOUBLE,
    
        ///<summary>
        /// Thicker than normal, but not as thick as heavy or bold.
        ///</summary>
        WAVY_HEAVY,
    
        ///<summary>
        /// The underlining line consists of a series of dashes placed over individual words in the text.
        ///</summary>
        WORDS
    }
}